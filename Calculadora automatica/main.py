print('Exercicio Calculadora')

number_1 = float(input('Insira o primeiro numero: '))
number_2 = float(input('Insira o segundo numero: '))

print('{} + {} = '.format(number_1,number_2))
print(number_1 + number_2)

print('{} - {} = '.format(number_1,number_2))
print(number_1 - number_2)

print('{} x {} = '.format(number_1,number_2))
print(number_1 * number_2)

print('{} / {} = '.format(number_1,number_2))
print(number_1 / number_2)


